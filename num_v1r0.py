# операции с числами чисел
num1 = 12
num2 = 3.4

print("num1 = ", num1)
print("num2 = ", num2)

print("num1 + num2 = ", num1 + num2)  # сложение
print("num1 - num2 = ", num1 - num2)  # вычитание
print("num1 * num2 = ", num1 * num2)  # умножение
print("num1 / num2 = ", num1 / num2)  # деление
print("num1 ^ num2 = ", num1 ** num2)  # возведение в степень
print("num1 // num2 = ", num1 // num2)  # целая часть от деления
print("num1 % num2 = ", num1 % num2)  # остаток от деления
